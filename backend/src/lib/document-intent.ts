/**
 * Document Intent Classifier
 *
 * Determines what the user wants to do with uploaded documents:
 * check, summarize, improve, write, or ask a question about them.
 * Uses keyword heuristics (no LLM call) for zero latency.
 */

export type DocumentIntent =
  | "check"       // Проверить документ на соответствие регламентам
  | "summarize"   // Суммаризировать / дать краткое содержание
  | "improve"     // Улучшить текст, стилистика, структура
  | "write"       // Написать / составить новый документ на основе загруженного
  | "analyze"     // Общий анализ содержания документа
  | "question";   // Задать вопрос по содержанию документа

export interface DocumentIntentResult {
  intent: DocumentIntent;
  confidence: number;
}

/**
 * Classify what the user wants to do with an uploaded document.
 * Pure heuristic approach for instant results.
 */
export function classifyDocumentIntent(
  userMessage: string,
  hasAttachments: boolean
): DocumentIntentResult {
  if (!hasAttachments) {
    return { intent: "question", confidence: 0 };
  }

  const lower = userMessage.toLowerCase().trim();

  // ── Summarize ──
  if (
    /суммар|кратк|резюм|обзор|основн\w+ (мысл|тезис|положени|пункт)|о чём|о чем|перескаж|пересказ|содержани|выдели (главн|ключев)|тезисн|brief|summar|краткое (содержание|описание|изложение)/i.test(lower)
  ) {
    return { intent: "summarize", confidence: 0.9 };
  }

  // ── Improve ──
  if (
    /улучш|исправ|отредактир|редактур|доработ|перепиш|переформулир|перефразир|стилист|грамм|орфограф|ошибк|поправ|причеш|вычит|корректир|правк|improve|rewrite|polish|refine/i.test(lower)
  ) {
    return { intent: "improve", confidence: 0.9 };
  }

  // ── Write / compose new document ──
  if (
    /напиши|составь|подготов|создай|сформир|разработай|оформи|draft|compose|генерир|шаблон|по (образцу|аналогии|примеру)|на основ[ае]|по мотивам/i.test(lower)
  ) {
    return { intent: "write", confidence: 0.85 };
  }

  // ── Check against regulations ──
  if (
    /провер|соответств|сравн|комплаенс|нарушен|отклонени|замечани|аудит|экспертиз|валидац|верифик|compliance|check|audit|верн[оы]\s*(ли|это)|правильн/i.test(lower)
  ) {
    return { intent: "check", confidence: 0.9 };
  }

  // ── Analyze ──
  if (
    /проанализ|анализ|разбер|разбор|оцен[иь]|рассмотр|изуч|что (думаешь|скажешь)|мнение|комментар|analyze|review|assess/i.test(lower)
  ) {
    return { intent: "analyze", confidence: 0.8 };
  }

  // ── Question about document content ──
  if (
    /\?|что|как|где|когда|зачем|почему|какой|сколько|кто|можно ли/i.test(lower)
  ) {
    return { intent: "question", confidence: 0.6 };
  }

  // Default: if user just attached a file with minimal or no text, assume check
  if (lower.length < 20) {
    return { intent: "check", confidence: 0.5 };
  }

  return { intent: "question", confidence: 0.4 };
}

/**
 * Generate system prompt instructions based on document intent.
 */
export function getDocumentIntentPrompt(
  intent: DocumentIntent,
  docCount: number
): string {
  switch (intent) {
    case "check":
      return `
РЕЖИМ ПРОВЕРКИ ДОКУМЕНТОВ:
Пользователь загрузил ${docCount} документ(ов) для проверки. Эти документы находятся в секции <uploaded_documents>.
Регламенты, стандарты и нормативные документы из базы знаний находятся в секции <documents>.

ТВОЯ ЗАДАЧА:
1. Тщательно проанализировать загруженные документы (<uploaded_documents>)
2. Сравнить их содержание с регламентами и стандартами из базы знаний (<documents>)
3. Выявить:
   - Соответствия и несоответствия требованиям регламентов
   - Отсутствующие обязательные пункты или разделы
   - Нарушения процедур, сроков, форматов
   - Возможные противоречия с законодательством (если применимо)
4. Ссылайся на конкретные пункты/разделы как загруженного документа, так и регламентов из базы знаний
5. Если в базе знаний не найдено подходящих регламентов — сообщи об этом явно
6. Структурируй ответ: сначала общая оценка, затем детальный анализ по пунктам

ФОРМАТ ОТВЕТА ПРИ ПРОВЕРКЕ:
- Начни с краткого резюме (соответствует / частично соответствует / не соответствует)
- Используй разделы: "Соответствия", "Несоответствия", "Рекомендации"
- Приводи дословные цитаты из обоих источников для обоснования`;

    case "summarize":
      return `
РЕЖИМ СУММАРИЗАЦИИ ДОКУМЕНТОВ:
Пользователь загрузил ${docCount} документ(ов) и хочет получить краткое содержание. Документы находятся в секции <uploaded_documents>.
При наличии релевантных регламентов в <documents>, используй их для контекста.

ТВОЯ ЗАДАЧА:
1. Определить тип документа (договор, положение, приказ, протокол, письмо и т.д.)
2. Выделить ключевые положения и основные тезисы
3. Указать стороны / участников (если применимо)
4. Перечислить важные сроки, суммы, обязательства
5. Отметить необычные или важные условия

ФОРМАТ ОТВЕТА:
- Начни с одного предложения: тип документа и его суть
- Далее раздел «Ключевые положения» с пронумерованным списком
- Если есть таблицы с данными — сохрани их в кратком виде
- В конце — краткий вывод: на что обратить особое внимание
- Если в базе знаний есть релевантные регламенты, укажи, каким требованиям документ должен соответствовать`;

    case "improve":
      return `
РЕЖИМ УЛУЧШЕНИЯ ДОКУМЕНТА:
Пользователь загрузил ${docCount} документ(ов) для улучшения текста. Документы находятся в секции <uploaded_documents>.
Регламенты из базы знаний (<documents>) используй как эталон стиля и структуры.

ТВОЯ ЗАДАЧА:
1. Оценить качество текста: структура, логика изложения, ясность формулировок
2. Выявить проблемы:
   - Стилистические неточности, канцеляризмы, тавтологии
   - Неполные или двусмысленные формулировки
   - Нарушения структуры (отсутствие нумерации, непоследовательность разделов)
   - Юридические неточности (если документ нормативный)
3. Предложить конкретные исправления с примерами
4. Если в базе знаний есть аналогичные документы — указать, как обычно формулируются подобные положения в регламентах СГК

ФОРМАТ ОТВЕТА:
- Общая оценка качества документа (1 абзац)
- Раздел «Замечания» с конкретными примерами: "Было → Рекомендуется"
- Раздел «Структурные рекомендации» (если применимо)
- По возможности, приведи исправленную версию ключевых фрагментов

ВАЖНО: Ты МОЖЕШЬ использовать свои знания русского языка и делового стиля для рекомендаций по улучшению текста. Правило «только на основе <documents>» НЕ применяется к стилистическим и грамматическим рекомендациям.`;

    case "write":
      return `
РЕЖИМ СОСТАВЛЕНИЯ ДОКУМЕНТА:
Пользователь загрузил ${docCount} документ(ов) как образец или основу для нового документа. Эти документы находятся в секции <uploaded_documents>.
Используй регламенты из базы знаний (<documents>) для обеспечения соответствия требованиям.

ТВОЯ ЗАДАЧА:
1. Понять, какой документ нужно составить и на основе чего
2. Использовать загруженные документы как шаблон / образец / исходные данные
3. Использовать нормативные документы из базы знаний для обеспечения соответствия стандартам СГК
4. Составить документ с правильной структурой, нумерацией, формулировками

ФОРМАТ ОТВЕТА:
- Составь полный текст документа в формате Markdown
- Используй правильную нумерацию разделов и пунктов
- Включи все обязательные разделы согласно регламентам из базы знаний
- Если чего-то не хватает в исходных данных — укажи, какую информацию нужно дополнить
- В конце добавь примечание, какие регламенты были учтены

ВАЖНО: Ты МОЖЕШЬ составлять текст документа, используя загруженные материалы и базу знаний. Правило «только на основе <documents>» применяется к фактической информации, но НЕ запрещает составление текста по образцу.`;

    case "analyze":
      return `
РЕЖИМ АНАЛИЗА ДОКУМЕНТА:
Пользователь загрузил ${docCount} документ(ов) для анализа. Документы находятся в секции <uploaded_documents>.
Используй регламенты из базы знаний (<documents>) для контекста и сопоставления.

ТВОЯ ЗАДАЧА:
1. Определить тип, назначение и контекст документа
2. Провести содержательный анализ: что документ устанавливает, регулирует, описывает
3. Выявить сильные и слабые стороны
4. Сопоставить с аналогичными документами или стандартами из базы знаний (если найдены)
5. Дать рекомендации

ФОРМАТ ОТВЕТА:
- Краткое описание документа (тип, дата, стороны)
- Содержательный анализ по разделам
- Сопоставление с регламентами из базы знаний
- Выводы и рекомендации`;

    case "question":
    default:
      return `
РЕЖИМ РАБОТЫ С ЗАГРУЖЕННЫМИ ДОКУМЕНТАМИ:
Пользователь загрузил ${docCount} документ(ов) и задаёт вопрос. Документы находятся в секции <uploaded_documents>.
Регламенты из базы знаний находятся в секции <documents>.

ТВОЯ ЗАДАЧА:
1. Ответить на вопрос пользователя, используя содержание загруженных документов
2. При необходимости сопоставить с регламентами из базы знаний
3. Цитировать конкретные пункты из загруженных документов
4. Если вопрос выходит за рамки загруженных документов — использовать базу знаний`;
  }
}
