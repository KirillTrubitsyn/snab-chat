-- 1. ПРОВЕРКА: Какие коды уже есть в таблице
SELECT
  'НАЙДЕН' AS статус,
  ic.code AS код,
  ic.name AS имя,
  ic.organization AS организация,
  ic.is_active AS активен,
  ic.uses_remaining AS осталось_использований,
  ic.device_limit AS лимит_устройств,
  CASE WHEN ic.password_hash IS NOT NULL THEN 'да' ELSE 'нет' END AS пароль_установлен
FROM invite_codes ic
WHERE ic.code IN (
  '299510', '4766F0', '8772D9', 'B234EB', '9C27C5',
  'D43D65', '0571A7', 'FC0E71', '992BC7', 'AE637D',
  '213244', '5591AF', 'AEE6F2', 'F558E2', '244758'
)
ORDER BY ic.code;

-- 2. ПРОВЕРКА: Какие коды ОТСУТСТВУЮТ
SELECT 'ОТСУТСТВУЕТ' AS статус, code AS код
FROM (
  VALUES
    ('299510'), ('4766F0'), ('8772D9'), ('B234EB'), ('9C27C5'),
    ('D43D65'), ('0571A7'), ('FC0E71'), ('992BC7'), ('AE637D'),
    ('213244'), ('5591AF'), ('AEE6F2'), ('F558E2'), ('244758')
) AS expected(code)
WHERE code NOT IN (SELECT ic.code FROM invite_codes ic);

