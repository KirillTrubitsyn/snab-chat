import { google, withGoogleApiLimit } from "@/app/lib/google-ai";
import { generateText } from "ai";

export async function autoTag(markdown: string): Promise<string[]> {
  const preview = markdown.slice(0, 4000);

  try {
    const { text } = await withGoogleApiLimit(() => generateText({
      model: google("gemini-3-flash-preview"),
      prompt: `Ты — эксперт по классификации корпоративных документов в области закупок и снабжения.

Проанализируй текст документа и сгенерируй от 3 до 8 тегов (на русском языке).

ПРАВИЛА:
1. Теги должны быть КОНКРЕТНЫМИ и отражать уникальную специфику именно этого документа
2. НЕ используй слишком общие теги вроде "документ", "информация", "данные", "текст", "файл"
3. Каждый тег — 1-3 слова, описывающие конкретную тему, процесс или объект документа
4. Включай: тип документа (спецификация, методика, регламент, ТЗ, договор, инструкция и т.д.)
5. Включай: предметную область (что именно закупается, какой процесс описан)
6. Включай: ключевые сущности (оборудование, материалы, услуги, конкретные категории товаров)

ПРИМЕРЫ хороших тегов: "спецификация оборудования", "критерии отбора поставщиков", "тендерная документация", "трубопроводная арматура", "оценка квалификации", "договор поставки", "техническое задание", "входной контроль", "логистика", "сметный расчёт"

ПРИМЕРЫ плохих тегов (НЕ использовать): "документ", "закупки", "снабжение", "корпоративное управление", "информация"

Верни ТОЛЬКО JSON-массив строк, без пояснений.

Текст документа:
${preview}`,
    }));

    const cleaned = text.replace(/```json?\s*/g, "").replace(/```/g, "").trim();
    const parsed = JSON.parse(cleaned);
    if (Array.isArray(parsed)) {
      return parsed.filter((t): t is string => typeof t === "string").slice(0, 10);
    }
  } catch (err) {
    console.error("autoTag error:", err);
  }
  return ["документ"];
}
